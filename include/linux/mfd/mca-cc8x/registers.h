#define MCA_CC8X_IRQ_MASK_0		0x0024
#define MCA_CC8X_IRQ_MASK_1		0x0025
#define MCA_CC8X_IRQ_MASK_2		0x0026
#define MCA_CC8X_IRQ_MASK_3		0x0027

#define MCA_CC8X_CTRL_UNLOCK_0		0x002d
#define MCA_CC8X_CTRL_UNLOCK_1		0x002e
#define MCA_CC8X_CTRL_UNLOCK_2		0x002f
#define MCA_CC8X_CTRL_UNLOCK_3		0x0030

#define MCA_CC8X_CTRL_0			0x0031
#define MCA_CC8X_CTRL_1			0x0032

#define MCA_CC8X_TIMER_TICK_0		0x0057
