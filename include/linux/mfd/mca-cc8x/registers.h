/* MCA_CC8X_IRQ_MASK_0 (addr=0x0024) */
#define MCA_CC8X_M_RTC_ALARM		BIT(0)
#define MCA_CC8X_M_RTC_1HZ		BIT(1)
#define MCA_CC8X_M_WATCHDOG		BIT(2)
