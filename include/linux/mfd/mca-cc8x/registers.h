#define MCA_CC8X_M_ADC			BIT(7)

/* MCA_CC8X_IRQ_MASK_2 */
#define MCA_CC8X_M_TAMPER2		BIT(0)
#define MCA_CC8X_M_TAMPER3		BIT(1)
#define MCA_CC8X_M_UART			BIT(2)


/* MCA_CC8X_CTRL_0 (addr=0x0031) */
#define MCA_CC8X_RESET			BIT(0)
