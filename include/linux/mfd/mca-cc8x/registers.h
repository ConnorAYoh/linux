#define MCA_CC8X_LAST_WAKEUP_PWRIO	BIT(0)
#define MCA_CC8X_LAST_WAKEUP_TIMER	BIT(1)
#define MCA_CC8X_LAST_WAKEUP_RTC	BIT(2)
#define MCA_CC8X_LAST_WAKEUP_LPUART	BIT(3)
#define MCA_CC8X_LAST_WAKEUP_TAMPER0	BIT(4)
#define MCA_CC8X_LAST_WAKEUP_TAMPER1	BIT(5)
#define MCA_CC8X_LAST_WAKEUP_TAMPER2	BIT(6)
