#define MCA_CC8X_LAST_MCA_RST_SW	BIT(10)
#define MCA_CC8X_LAST_MCA_RST_MDMAPP	BIT(11)
#define MCA_CC8X_LAST_MCA_RST_SMAE	BIT(13)

/* MCA_CC8X_LAST_MPU_RESET_n (addr=0x009c & 0x009f) */
