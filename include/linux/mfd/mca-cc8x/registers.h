#define MCA_CC8X_LAST_WAKEUP_IO14	BIT(22)
#define MCA_CC8X_LAST_WAKEUP_IO15	BIT(23)
#define MCA_CC8X_LAST_WAKEUP_IO16	BIT(24)
#define MCA_CC8X_LAST_WAKEUP_IO17	BIT(25)
#define MCA_CC8X_LAST_WAKEUP_IO18	BIT(26)
#define MCA_CC8X_LAST_WAKEUP_IO19	BIT(27)
#define MCA_CC8X_LAST_WAKEUP_IO20	BIT(28)
#define MCA_CC8X_LAST_WAKEUP_IO21	BIT(29)
#define MCA_CC8X_LAST_WAKEUP_VCC	BIT(30)

#endif /* MCA_CC8X_REGISTERS_H_ */
